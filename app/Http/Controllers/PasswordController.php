     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $password)
    {

        $request->validate([
            'old_password' => ['required', new MatchOldPassword($password->password)],
            'password' => 'required|string|min:8|confirmed',
        ]);
        $hashedPassword = Hash::make($request->password);
        $password->update(['password' => $hashedPassword]);

        return redirect()->back()->with('success', 'User password has been updated successfully!');
    }

